    public int check_username_availability(String username){
                int chk=0;
                Connection conn= null;
                Statement stmt= null;
                try {
                    Class.forName("oracle.jdbc.OracleDriver");
                    conn= DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","konvictstar");
                    stmt = conn.createStatement();

                    String sql_query;
                    sql_query="select username from MEMBER where username='"+username+"'";
                    ResultSet rs= stmt.executeQuery(sql_query);
                    if(!rs.next()) {
                        chk=1;
                    }
                    rs.close();
                    stmt.close();
                    conn.close();
                } catch (SQLException se) {
                    se.printStackTrace();
                } catch (Exception e) {
                    e.printStackTrace();
                }
                if(chk==0)
                    jLabel9.setText("username not available");
                else 
                    jLabel9.setText("username available");
                    
        return chk;
    }
